import java.math.BigInteger;

class dict2cube{
	BigInteger c;
	BigInteger e1;
	BigInteger e2;
	BigInteger e3;
	BigInteger oldstep;
	int f1;
	public dict2cube(BigInteger c, BigInteger e1, BigInteger e2, BigInteger e3, BigInteger oldstep, int f1) {
		this.c=c;
		this.e1=e1;
		this.e2=e2;
		this.e3=e3;
		this.oldstep=oldstep;
		this.f1=f1;
	}
}